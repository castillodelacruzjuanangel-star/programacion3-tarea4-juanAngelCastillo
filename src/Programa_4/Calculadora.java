package Programa_4;

public class Calculadora {

    //metodos estaticos que relizan las operaciones de basicas y retornan el resultado
    //cada metodo recibe dos numeros como parametros
    
    public static double suma(int num1, int num2){
        return num1 + num2;
    }

    public static double resta(int num1, int num2){
        return num1 - num2;
    }

    public static double multiplicacion(int num1, int num2){
        return num1 * num2;
    }

    public static double division(int num1, int num2){
        return num1 / num2;
    }
}
